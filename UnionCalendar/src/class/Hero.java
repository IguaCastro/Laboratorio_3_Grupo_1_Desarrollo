public class Hero {
    private int level;
    private int power;
    private String name;
    private String range;

    public Hero(int level, int power, String name, String range){
        this.level = level;
        this.power = power;
        this.name = name;
        this.range = range;
    }

    public int getLevel(){
        return this.level = level;
    }
    public void setLevel(int level){
        this.level = level;

    }
    public int getPower(){
        return this.power;
    }
    public void setPower(int level){
        this.power = power;
    }

    public String getName(){
        return this.name = name;
    }
    public void setName(){
        this.name = name;
    }

    public String getRange(){
        return this.range = range;
    }
    public void setRange(){
        this.range = range;
    }
    
}
